     * @var array
     */
    public static $rules = [
        'documentId' => 'required',
        'languageCode' => 'required',
        'description' => 'required'
    ];

    public function documentCodeModule()
    {
        return $this->belongsTo(DocumentCodeModule::class, 'documentId', 'id');
    }
    
}
